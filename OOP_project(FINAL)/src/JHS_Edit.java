    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
        // Check if all necessary inputs are filled
        if (inputName.getText().isEmpty() ||
            inputBirthDate.getText().isEmpty() ||
            inputBirthPlace.getText().isEmpty() ||
            inputCurrentAddress.getText().isEmpty() ||
            inputPermanentAddress.getText().isEmpty() ||
            inputAge.getText().isEmpty() ||
            inputCitizenship.getText().isEmpty() ||
            inputReligion.getText().isEmpty() ||
            inputHeight.getText().isEmpty() ||
            inputWeight.getText().isEmpty() ||
            inputCPnum.getText().isEmpty() ||
            inputEmail.getText().isEmpty() ||
            inputSchool.getText().isEmpty() ||
            inputAccomplish.getText().isEmpty() ||
            inputDateOfAttendance1.getText().isEmpty() ||
            inputGraduate.getText().isEmpty() ||
            inputSchool2.getText().isEmpty() ||
            inputAccomplish2.getText().isEmpty() ||
            inputDateOfAttendance2.getText().isEmpty() ||
            inputGraduate2.getText().isEmpty()) {

            // Show error message if any input is missing
            JOptionPane.showMessageDialog(this, "You need to fill up all necessary data", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (jRadioButton1.isSelected()) {
                sex = 'M';
            } else {
                sex = 'F';
            }

            switch (comboBoxBloodType.getSelectedIndex()) {
                case 0:
                bloodtype = "A+";
                break;
                case 1:
                bloodtype = "A";
                break;
                case 2:
                bloodtype = "B+";
                break;
                case 3:
                bloodtype = "B";
                break;
                case 4:
                bloodtype = "O+";
                break;
                case 5:
                bloodtype = "O";
                break;
                case 6:
                bloodtype = "AB+";
                break;
                case 7:
                bloodtype = "AB ";
                break;
            }

            switch (comboBoxSpec.getSelectedIndex()) {
                case 0:
                specialization = "RBEC";
                break;
                case 1:
                specialization = "SPA";
                break;
                case 2:
                specialization = "STE";
                break;
            }

            switch (comboBoxGradeLevel.getSelectedIndex()) {
                case 0:
                gradeLevel = 7;
                break;
                case 1:
                gradeLevel = 8;
                break;
                case 2:
                gradeLevel = 9;
                break;
                case 3:
                gradeLevel = 10;
                break;
            }
            
            jhsStudent.LRN = inputLRN.getText();
            jhsStudent.name = inputName.getText();
            jhsStudent.birthdate = inputBirthDate.getText();
            jhsStudent.placeOfBirth = inputBirthPlace.getText();
            jhsStudent.age = Integer.parseInt(inputAge.getText());
            jhsStudent.currentAddress = inputCurrentAddress.getText();
            jhsStudent.permanentAddress = inputPermanentAddress.getText();
            jhsStudent.sex = sex;
            jhsStudent.citizenship = inputCitizenship.getText();
            jhsStudent.religion = inputReligion.getText();
            jhsStudent.height = Double.parseDouble(inputHeight.getText());
            jhsStudent.weight = Double.parseDouble(inputWeight.getText());
            jhsStudent.bloodType = bloodtype;
            jhsStudent.contactNumber = inputCPnum.getText();
            jhsStudent.email = inputEmail.getText();
            jhsStudent.school1 = inputSchool.getText();
            jhsStudent.accomplishments1 = inputAccomplish.getText();
            jhsStudent.dateOfAttendance1 =  inputDateOfAttendance1.getText();
            jhsStudent.graduationDate1 = inputGraduate.getText();
            jhsStudent.school2 = inputSchool2.getText();
            jhsStudent.accomplishments2 = inputAccomplish2.getText();
            jhsStudent.dateOfAttendance2=  inputDateOfAttendance2.getText();
            jhsStudent.graduationDate2 = inputGraduate2.getText();
            jhsStudent.gradeLevel = gradeLevel;
            jhsStudent.spec = specialization;
            
            jhsStudent.clearFile();

            // Remove duplicates based on LRN to ensure no redundancy
            Iterator<Student> iterator = student.iterator();
            while (iterator.hasNext()) {
                Student stud = iterator.next();
                if (stud.LRN.equals(jhsStudent.LRN) && stud != jhsStudent) { // Avoid removing the newly added student
                    iterator.remove(); // Safely remove the duplicate
                }
            }
            // Save the JHS students to the file using the `savestudent` method
            try {
                for (Student stud : student) {
                    if (stud instanceof JHS) { // Check if the student is an instance of JHS
                        JHS jhsStud = (JHS) stud; // Cast to JHS
                        jhsStud.savestudent(); // Use the provided savestudent method
                    }
                }
                
                JOptionPane.showMessageDialog(null, "JHS students saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                Admin admin = new Admin(student);
                admin.setVisible(true);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error saving students: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                Admin admin = new Admin(student);
                admin.setVisible(true);
                this.dispose();
            }

        }
    }//GEN-LAST:event_ConfirmActionPerformed

